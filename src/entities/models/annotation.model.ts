/** Аннотация, привязанная к конкретному диапазону текста внутри статьи. */
export interface Annotation {
  /** Уникальный идентификатор (UUID v4). */
  id: string;
  /** Идентификатор статьи, к которой относится аннотация. */
  articleId: string;
  /** Начальная позиция диапазона в строке контента статьи (включительно). */
  startOffset: number;
  /** Конечная позиция диапазона в строке контента статьи (не включая). */
  endOffset: number;
  /** CSS-совместимая строка цвета (например, `#e74c3c`) для подчёркивания. */
  color: string;
  /** Текстовая заметка, написанная пользователем для выделенного фрагмента. */
  note: string;
  /** Снимок выделенного текста на момент создания аннотации. */
  selectedText: string;
}
