/**
 * Данные о текущем выделении текста пользователем до подтверждения аннотации.
 * Позиции являются символьными смещениями внутри строки контента статьи.
 */
export interface PendingSelection {
  /** Начальная позиция выделения внутри контента статьи. */
  startOffset: number;
  /** Конечная позиция выделения внутри контента статьи. */
  endOffset: number;
  /** Сырой выделенный текст, возвращённый Selection API. */
  selectedText: string;
  /** Горизонтальная координата viewport (px) для позиционирования попапа. */
  x: number;
  /** Вертикальная координата viewport (px) для позиционирования попапа. */
  y: number;
}
