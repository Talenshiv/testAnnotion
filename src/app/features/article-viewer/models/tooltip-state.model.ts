/** Состояние оверлея подсказки, отображаемой над аннотированным фрагментом. */
export interface TooltipState {
  /** Текст заметки аннотации для отображения. */
  note: string;
  /** Строка цвета (hex), связанная с аннотацией. */
  color: string;
  /** Горизонтальная координата viewport (px) для позиционирования подсказки. */
  x: number;
  /** Вертикальная координата viewport (px) для позиционирования подсказки. */
  y: number;
}
